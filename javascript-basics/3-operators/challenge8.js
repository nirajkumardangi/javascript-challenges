/* Challenge 8: Decrement Operator
Task: Create a variable countDown and initialize it with the value 10. Use the decrement operator to decrease countDown by 1 twice. Print countDown after each decrement. */

// SOLUTION

let countDown = 10;
countDown--;
countDown--;
console.log(countDown);
countDown--;
countDown--;
console.log(countDown);

// OUTPUT: 
// 8
// 6